%Function to calculate using Euler's Method
function eul(x0,b,f,h,y,x,n,g)
T=[];

for i=1:n
    y=y+h*f(x,y);
    x=x0+h*i;
    
    %Error: True Value of at x-Calculated value at x
    p=g(x)-y;
     
    
    if (rem(x,0.25)==0)
        T=[T;x y p];
        
    end   
    
end  

T
end   

 
